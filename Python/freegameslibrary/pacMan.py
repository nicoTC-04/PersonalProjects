# hacer cuadrados que van a ser el mapa/laberinto
def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# regresa el offset de un punto del mapa
def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

# regresa true si el punto es valido (si si es parte del mapa)
def valid(point):
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# dibuja el mapa
def world():
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# mueve el pacman y los fantasmas
def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # codigo editado para hacer fantasmas mas rapidos
    for point, course in ghosts:
        # se mueve el fantasma
        point.move(course)
        # arreglo de opciones para la direccion
        options = [
            vector(10, 0),
            vector(-10, 0),
            vector(0, 10),
            vector(0, -10),
        ]
        # arreglo para ver si la direccion es validad
        validCourses = [
            valid(point + options[0]),
            valid(point + options[1]),
            valid(point + options[2]),
            valid(point + options[3]),
        ]

        # variables temporales para hacer fantasmas más inteligentes
        tempOption = 0
        tempDistanceToPac = abs(pacman - options[0])

        # calcular cual es la direccion que hace más pequeña la distancia a PacMan
        for i in range(len(options)):
            if (abs(pacman - options[i]) <= tempDistanceToPac and validCourses[i]):
                tempOption = i
                tempDistanceToPac = abs(pacman - options[i])

        # definir nueva direccion
        course.x = options[tempOption].x
        course.y = options[tempOption].y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

# cambia la direccion del pacman
def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


